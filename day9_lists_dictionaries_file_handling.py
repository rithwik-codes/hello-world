# ðŸ‹ï¸ Mini Project - Student Report Card System

# Step 1: Store student data in a dictionary
student = {
    "name": input("Enter student name: "),
    "maths": int(input("Enter Maths marks: ")),
    "science": int(input("Enter Science marks: ")),
    "english": int(input("Enter English marks: "))
}

# Step 2: Calculate average and grade
average = (student["maths"] + student["science"] + student["english"]) / 3

if average >= 90:
    grade = "A"
elif average >= 75:
    grade = "B"
elif average >= 50:
    grade = "C"
else:
    grade = "F"

student["average"] = average
student["grade"] = grade

# Step 3: Write to file
with open("report_card.txt", "w") as file:
    file.write("Student Report Card\n")
    file.write("====================\n")
    for key, value in student.items():
        file.write(f"{key}: {value}\n")

print("Report card saved to report_card.txt âœ…")

# Step 4: Read back the file
with open("report_card.txt", "r") as file:
    report = file.read()

print("\n--- Report Card ---")
print(report)

